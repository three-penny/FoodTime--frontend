/**
 * 根据食堂 id 获取食堂资料。
 * @param {string} canteenId 食堂 id。
 * @returns {object|null} 食堂对象或 null。
 */
export function getCanteenById(canteenId) {
  return canteenList.find((item) => item.id === canteenId) || null;
}

/**
 * 获取某食堂下的菜品列表。
 * @param {string} canteenId 食堂 id。
 * @returns {Array} 菜品数组。
 */
export function getDishesByCanteenId(canteenId) {
  return dishList.filter((item) => item.canteenId === canteenId);
}

/**
 * 根据食堂 id 与菜品 id 获取单菜详情。
 * @param {string} canteenId 食堂 id。
 * @param {string} dishId 菜品 id。
 * @returns {object|null} 菜品对象或 null。
 */
export function getDishById(canteenId, dishId) {
  return (
    dishList.find(
      (item) => item.canteenId === canteenId && item.id === dishId,
    ) || null
  );
}

/**
 * 获取食堂菜品分类。
 * @param {string} canteenId 食堂 id。
 * @returns {Array<string>} 分类列表。
 */
export function getDishCategories(canteenId) {
  return Array.from(
    new Set(getDishesByCanteenId(canteenId).map((item) => item.category)),
  );
}

/**
 * 获取关联菜品。
 * @param {string} canteenId 食堂 id。
 * @param {string} dishId 菜品 id。
 * @param {number} limit 最大返回数。
 * @returns {Array} 关联菜品。
 */
export function getRelatedDishes(canteenId, dishId, limit = 3) {
  const currentDish = getDishById(canteenId, dishId);
  const sameCanteenDishes = getDishesByCanteenId(canteenId).filter(
    (item) => item.id !== dishId,
  );

  if (!currentDish) {
    return sameCanteenDishes.slice(0, limit);
  }

  const sameCategory = sameCanteenDishes.filter(
    (item) => item.category === currentDish.category,
  );
  const fallback = sameCanteenDishes.filter(
    (item) => item.category !== currentDish.category,
  );

  return [...sameCategory, ...fallback].slice(0, limit);
}

/**
 * 获取首页主推菜。
 * @param {number} limit 最大返回数。
 * @returns {Array} 推荐菜品。
 */
export function getHomeRecommendedDishes(limit = 6) {
  return [...dishList]
    .sort((left, right) => {
      const leftScore = left.rating * 100 + left.monthlySales;
      const rightScore = right.rating * 100 + right.monthlySales;
      return rightScore - leftScore;
    })
    .slice(0, limit);
}

/**
 * 获取销量榜。
 * @param {number} limit 最大返回数。
 * @returns {Array} 榜单菜品。
 */
export function getTopRankedDishes(limit = 10) {
  return [...dishList]
    .sort((left, right) => {
      const leftScore = left.rating * 1000 + left.monthlySales;
      const rightScore = right.rating * 1000 + right.monthlySales;
      return rightScore - leftScore;
    })
    .slice(0, limit);
}

/**
 * 获取今日菜单条目。
 * @param {number} limit 最大返回数。
 * @returns {Array} 菜单条目。
 */
export function getDailyMenu(limit = 8) {
  return getHomeRecommendedDishes(limit).map((dish) => {
    const canteen = getCanteenById(dish.canteenId);

    return {
      ...dish,
      canteenName: canteen?.name || '',
      menuIntro: `${canteen?.name || ''} · ${dish.windowName}`,
    };
  });
}

